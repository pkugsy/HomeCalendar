package pku.gsy.app.util.date;

public class GDateInfo {
	public int gYear = 1900;
	public int gMonth = 1;
	public int gDay = 1;
	
	public GDateInfo() {
	}
	
	public GDateInfo(int gYear, int gMonth, int gDay) {
		this.gYear = gYear;
		this.gMonth = gMonth;
		this.gDay = gDay;
	}
	
}
